package model;

/**
 *
 * @author Trong Thinh
 */
public class Order {

    private String uId;
    private String pId;
    private int amount;
    private int id;

    public Order() {
    }

    public Order(String uId, String pId, int amount, int id) {
        this.uId = uId;
        this.pId = pId;
        this.amount = amount;
        this.id = id;
    }

    public String getuId() {
        return uId;
    }

    public void setuId(String uId) {
        this.uId = uId;
    }

    public String getpId() {
        return pId;
    }

    public void setpId(String pId) {
        this.pId = pId;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
